ll bruh(ll n) {
    string s = to_string(n);
    ll ans = 0;
    for(int i = 0; i < s.size(); i++) {
        ans += s[i] - '0';
    }
    return ans;
}

void solve(){        
    ll n;
    cin >> n;
    ll ans = 0;
    for(int i = n + 1; i < n + 200; i++) {
        if(i - bruh(i) == n) ans ++;
    }
    cout << ans << endl;
    
}

